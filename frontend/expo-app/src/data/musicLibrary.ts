/**
 * musicLibrary.ts
 *
 * Static asset manifest for the local music library.
 *
 * All require() calls MUST be static literals — Metro bundler resolves them
 * at compile time and CANNOT handle dynamic require() with variable paths.
 *
 * Library: 90+ tracks across 5 moods
 *   calm      ~20 tracks  (ambient, lo-fi, Indian classical/Krishna flute)
 *   cinematic ~30 tracks  (epic, orchestral, trailer, war, triumphant)
 *   energetic ~25 tracks  (action, EDM, percussion, Bollywood beats)
 *   happy     ~21 tracks  (folk, pop, Indian festival, upbeat)
 *   sad       ~22 tracks  (piano solo, strings, bansuri, emotional)
 *
 * Sources: Kevin MacLeod (CC BY 4.0), Pixabay (CC0), various
 */

import type { ClassifiedMood } from '../engine/classifier';

